        <h2>Fachkompetenz</h2>
    <p>
        Die Fachkompetenz ist im Gegensatz zu der Methodenkompetenz das Wissen was man sich aneignen muss um die Methodenkompetenz überhaupt durchführen zu können. Das heisst die Fachkompetenz ist der Schlüssel für ein erfolgreiches Arbeiten. 
        <br>
        Hier noch ein Beispiel zu den Fachkompetenzen:
        <br>
        Ein lernender Koch kennt die wichtigsten französischen Weine mit ihren Traubensorten und weiss wozu welcher Wein am besten passt.
    </p>
        
        <h2>Methodenkompetenz</h2>
        
    <ul class="outlook">
            <li>-Die Fertigkeit, Informationen zu beschaffen zu strukturieren, zu bearbeiten, aufzubewahren, wiederzuverwenden und darzustellen.</li>
            <li>-die Fertigkeit, Ergebnisse von Verarbeitungsprozessen richtig zu interpretieren und in geeigneter Form zu präsentieren;</li>
            <li>-Die Fertigkeit zur Anwendung von Problemlösungstechniken</li>
            <li>-Die Fertigkeit zur Gestaltung von Problemlösungsprozessen z.B im Projektmanagement</li>
    </ul>
        
        <h2>Sozialkompetenz</h2>
    <p>
        Unter Sozialkompetenz werden all die Fähigkeiten zusammengefasst, die uns erlauben, effektiv mit anderen Menschen zusammen zu arbeiten. Die wichtigsten Punkte die man beachten muss um die Sozialkompetenz erreichen zu können ist die Konflikfähigkeit. 
        Sie zeigt wie gut ein Mensch auskommen kann in Diskussionen und Meinungsverschiedenheiten. Es setzt voraus dass man sachlich und professionell bei Auseinandersetzungen bleiben kann. Kommunikation im Team untereinander ist natürlich sehr wichtig für die produktive Arbeit in einer Gruppe. 
    </p>
        
        <h2>Persönlichkeitskompetenz</h2>
        <p>
        Die Fähigkeit der Persönlichkeitskompetenz setzt voraus dass man Persönlichkeitsmerkmale erkennen und diese situationsgerecht einsetzten kann. 
        Dies sind die wichtigsten Punkte zum aufweisen der Persönlichkeitskompetenz: die Belastbarkeit, das Durchsetzungsvermögen, die Eigenmotivation, das Selbstvertrauen und die Selbstreflektionsfähigkeit.
    </p>    
